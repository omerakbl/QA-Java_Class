package Class_Day_004;

public class AssigmentOperations {

    public static void main(String[] args) {

        int box = 10;
        box = 15; // do not need to specify the data type again
        box = box+10;
        System.out.println(box);

        box += 10; // same thing as previous just short way to write it.
        System.out.println(box);

        int box1 = 20;
        box1 = box1 - 10;
        box -= 10; // same thing as previous just short way to write it.
        System.out.println(box1);


        int num1 = 20;
        num1 %=3; // same thing as previous just short way to write it.
        System.out.println(num1);

    }
}
