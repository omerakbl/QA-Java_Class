package Test_Night_001;

public class tn_001_Rational_Operators {

    public static void main(String[] args) {

        //create variables
        int a=10;
        int b=15;

        //==equal operator
        System.out.println(a==b); // 10 == 15 false
        // != ( oppesite equal ---> not equal )
        System.out.println(a != b); // 10 != 15 true
        // > operator
        System.out.println(a > b); // 10 != 15 false
        // < operator
        System.out.println(a < b); // 10 != 15 true
        // >= operator
        System.out.println( a >= b); // 10 != 15 false it says if a bigger equal to b  then it should be false
        // <= operator
        System.out.println(a <= b); // 10 != 15 true it says if a bigger equal to b  then it should be true

    }
}
