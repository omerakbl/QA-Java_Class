package Class_Day_002;

public class Variables {
    public static void main(String[] args) {
        // boolean It is True/False. It is 1 bit.


        boolean bolValue = true;
        System.out.println(bolValue);
        boolean bolValue2 = false;
        System.out.println(bolValue2);


        // byte , It is 8 bit. values between -128 to 127
        byte byteValue = 120;
        System.out.println(byteValue);

        // char come from chracter, it is 16 byte, it contains chracters , numbers and so on
        char charValue = 'A';
        System.out.println(charValue);

        // short it takes values -32768 to 32767
        short shortValue = 3000;

        System.out.println(shortValue);

        // int it is shor integers. 32 bits
        int intValue = 40000;

        // long 64 bits memory,
        long longValue = 1231231231L;

        System.out.println(longValue);

        // float It is 32 bit it hold only 6 decimal dgits ,
        float floatValue =  123.3123132131321F;

        System.out.println(floatValue);

        // double 64 bit we can hold hold up to 16 digits after decimal point.
        double doubleValue = 123.1231231231331111;

        System.out.println(doubleValue);



        // non-primative
    }
}
